def get_access_token():
    if _token_cache["access_token"]:
        return _token_cache["access_token"]
    url = f"{KIS_BASE_URL}/oauth2/tokenP"
    body = {
        "grant_type": "client_credentials",
        "appkey": KIS_APP_KEY,
        "appsecret": KIS_APP_SECRET,
    }
    res = requests.post(url, json=body)
    if res.status_code != 200:
        raise HTTPException(status_code=500, detail=f"KIS 토큰 발급 실패: {res.status_code} / {res.text}")
    token = res.json().get("access_token")
    _token_cache["access_token"] = token
    return token


def kis_headers(tr_id: str):
    token = get_access_token()
    return {
        "authorization": f"Bearer {token}",
        "appkey": KIS_APP_KEY,
        "appsecret": KIS_APP_SECRET,
        "tr_id": tr_id,
        "Content-Type": "application/json; charset=utf-8",
    }


def is_korean(ticker: str) -> bool:
    return ticker.isdigit() and len(ticker) == 6


def normalize_ticker(q: str) -> str:
    q = q.strip()
    if q.upper().endswith(".KS") or q.upper().endswith(".KQ"):
        return q[:6]
    lower = q.lower()
    if lower in KR_TICKER_MAP:
        return KR_TICKER_MAP[lower]
    if q.isdigit() and len(q) == 6:
        return q
    return q.upper()


def safe_float(val, default=None):
    try:
        f = float(val)
        return f if f != 0 else default
    except:
        return default


